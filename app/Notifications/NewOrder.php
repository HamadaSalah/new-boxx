    // }

    /**
     * Get the array representation of the notification.
     *
     * @param  mixed  $notifiable
     * @return array
     */
    public function toArray($notifiable)
    {
        $MyOrder = Order::findOrFail($this->order->id);
        return [
            'id' => $this->order->id,
            'title' => 'There is New Order Added To Dashboard'
            .'<h4 class="myTask">'.$MyOrder->full_name.'</h4>',
            'created' => $MyOrder->created_at->isoFormat('dddd, MMMM Do YYYY, h:mm')
        ];
    }
}
